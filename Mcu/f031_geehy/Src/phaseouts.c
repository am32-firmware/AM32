void allOff()
{
    phaseAFLOAT();
    phaseBFLOAT();
    phaseCFLOAT();
}
// full braking shorting all low sides
void fullBrake()
{
    phaseALOW();
    phaseBLOW();
    phaseCLOW();
}
// for stepper_sine
void allpwm()
{
    phaseAPWM();
    phaseBPWM();
    phaseCPWM();
}

void twoChannelForward()
{
    phaseAPWM();
    phaseBLOW();
    phaseCPWM();
}

void twoChannelReverse()
{
    phaseALOW();
    phaseBPWM();
    phaseCLOW();
}

void comStep(int newStep)
{
    switch (newStep) 
    {
        case 1: // A-B
            phaseCFLOAT();
            phaseBLOW();
            phaseAPWM();
            break;

        case 2: // C-B
            phaseAFLOAT();
            phaseBLOW();
            phaseCPWM();
            break;

        case 3: // C-A
            phaseBFLOAT();
            phaseALOW();
            phaseCPWM();
            break;

        case 4: // B-A
            phaseCFLOAT();
            phaseALOW();
            phaseBPWM();
            break;

        case 5: // B-C
            phaseAFLOAT();
            phaseCLOW();
            phaseBPWM();
            break;

        case 6: // A-C
            phaseBFLOAT();
            phaseCLOW();
            phaseAPWM();
            break;
    }
}